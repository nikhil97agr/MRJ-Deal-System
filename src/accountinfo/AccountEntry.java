    //write account data to the file
    private void writeToFile(File file, String data) throws FileNotFoundException, IOException {
        byte[] dataBytes = new CipherData().encrypter(data.getBytes());
        DataOutputStream outputStream = new DataOutputStream(new BufferedOutputStream(new FileOutputStream(file, true)));
        outputStream.writeInt(dataBytes.length);
        outputStream.write(dataBytes);
        outputStream.flush();
        outputStream.close();
        clearInputs();

    }

    //clear all the input fields
    private void clearInputs() {
        nameInput.setText("");
        panInput.setText("");
        addressInput.setText("");
        selfCheck.setSelected(false);
        emailInputTF.setText("");
        mobileInputTF.setText("");
    }

