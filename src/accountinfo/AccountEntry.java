    //save new account data to the file
    private void saveData(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveData

        name = nameInput.getText();
        address = addressInput.getText();
        pan = panInput.getText();
        isSelf = selfCheck.isSelected();
        String email = emailInputTF.getText();
        String mobile = mobileInputTF.getText();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fields required");
            return;
        }
        if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fields required");
            return;
        }
        if (pan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fields required");
            return;
        }
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fields required");
            return;
        }
        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fields required");
            return;
        }

        if (mode != null) {
            accountData.get(index).setName(name);
            accountData.get(index).setAddress(address);
            accountData.get(index).setPan(pan);
            accountData.get(index).setIsSelf(isSelf);
            this.dispose();
            new showAccountdetails(accountData).setVisible(true);
            return;
        }

        File file = new File("account data.mno");


        if (!file.exists()) {
            try {
                file.createNewFile();
            } catch (IOException ex) {
                Logger.getLogger(AccountEntry.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        String data = name + ":" + address + ":" + pan + ":" + isSelf+":"+mobile+":"+email;
        AccountData accountData = new AccountData(name, address, pan, isSelf, mobile , email);
        boolean result = false;
        if (isSelf) {
            for (AccountData accData : this.accountData) {
                if (accData.isIsSelf()) {
                    accountData.setId(1);
                    result = true;
                    break;
                }
            }
            if (!result) {
                accountData.setId(0);
            }
        }
        data = data + ":" + accountData.getId();

        this.accountData.add(accountData);
        try {
            writeToFile(file, data);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(AccountEntry.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(AccountEntry.class.getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(this, "Saved!!");


    }//GEN-LAST:event_saveData

